package com.syntax.class07;

public class DoWhileLoop {
public static void main(String[] args) {
	//print hello 3Xs
	//while checks if soup hot before eating
	//do while eats soup before checking if its hot
	//
	
	
	int x = 11;
	while(x <= 3) {//checks before going into loop
		System.out.println("Hello");
		x++;
	}
	
	System.out.println("----------------using a Do While Loop------------");
	
	int y = 11;
	do {
		System.out.println("Hello");
		y++;
	} while (y <= 3);
	
	//checks after going into the loop
	
	
	
}





}
