package com.syntax.class01;

public class PrimativeDataTypes {
		
	public static void main(String [] argo) {
		//whole # data types
		
		byte num = 10;
		System.out.println(num);
		short num1 = 100;
		System.out.println(num1);
		int num2 = 2010;
		System.out.println(num2);
		long num3 = 1000000000000000000L;
		System.out.println(num3);
		//decimal # data types
		
		float num4 = 12.9F;
		System.out.println(num4);
		double num5 = 0.888;
		System.out.println(num5);
		// non-# data types
		
		char add = '+'; 
		char letter = 'a';
		System.out.println(add);
		System.out.println(letter);
		//true or fales (yes or no)
		
		boolean hunry = true;
		boolean thristy = false;
		System.out.println(hunry);
		System.out.println(thristy);
		
		
		
		
		
		
		
		
		
	}










}
